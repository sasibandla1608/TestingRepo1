package com.javaconcepts.stringsession;

public class CharAt {
	
    public static void main(String[] args) {
		 
    	 String country="India"; //length 5-2=4 index=0,1,2,3,4
    	 
    	 System.out.println(country.charAt(3));
    	 
    	 String name1=" Lakshmi,sayamma ";
    	 
    	 System.out.println(name1.charAt(10));
    	 
    	 String  animal="Donkey";
    	 
    	 System.out.println(animal.charAt(5));
    	 
    	 
	}

}
